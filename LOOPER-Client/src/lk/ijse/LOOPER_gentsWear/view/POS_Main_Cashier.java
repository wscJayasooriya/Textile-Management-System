    private void setDate() {
        Date d = new Date();
        SimpleDateFormat s = new SimpleDateFormat("E dd-MM-yyyy");
        lblDate.setText(s.format(d));

    }

    private void setTime() {
        new Timer(0, new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                Date d = new Date();
                SimpleDateFormat s = new SimpleDateFormat("hh:mm:ss a");
                lblTime.setText(s.format(d));
            }
        }).start();
    }

    private void loadAllDressCode() {

        cmbDressCode.removeAllItems();
        try {
            List<ItemDTO> all = ManageItemController.getAllItems();
            if (all != null) {
                for (ItemDTO itemDTO : all) {
                    cmbDressCode.addItem(itemDTO.getiCode());
                }

            }
        } catch (Exception ex) {
            Logger.getLogger(POS_Main_Cashier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void setAmount() {
        double total = 0;
        for (int i = 0; i < dtm.getRowCount(); i++) {
            total += Double.parseDouble(dtm.getValueAt(i, 4).toString());
        }
        txtG_Amount.setText(Double.toString(total));
    }

    private void selBalance() {
        double balnce = Double.parseDouble(txtCash.getText()) - Double.parseDouble(txtG_Amount.getText());
        txtChange.setText(Double.toString(balnce));
    }

    private void clearAlltextField() {
        txtInvoiceNo.setText("");
        txtDescription.setText("");
        txtPrice.setText("");
        cmbDressCode.setSelectedItem("Dresses");
        txtQuantity.setText("");
        txtQuantityOnHand.setText("");
        txtG_Amount.setText("");
        dtm.setRowCount(0);
        txtCash.setText("");
        txtChange.setText("");
        txtAmount.setText("");
        txtTotItem.setText("");

    }

    private int getDuplicateRow(Object selectedItem) {
        DefaultTableModel dtm = (DefaultTableModel) tblOrder.getModel();
        for (int i = 0; i < dtm.getRowCount(); i++) {
            if (dtm.getValueAt(i, 0).equals(selectedItem)) {
                return i;
            }
        }
        return -1;
    }

