    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        ManageItemController itemController = new ManageItemController();
        ManagePaymentController paymentController = new ManagePaymentController();
        ManagePlaceOrderController placeOrderController = new ManagePlaceOrderController();

        if (cmbDressCode.getSelectedIndex() != -1) {
            try {

                OrderDTO orderDTO = new OrderDTO();
                orderDTO.setoID(Integer.parseInt(txtInvoiceNo.getText()));
                orderDTO.setoDate(lblDate.getText());
                orderDTO.setoTime(lblTime.getText());
                orderDTO.setAmount(Double.parseDouble(txtAmount.getText()));

                PaymentDTO paymentDTO = new PaymentDTO();
                paymentDTO.setGrossAmount(Double.parseDouble(txtG_Amount.getText()));
                paymentDTO.setCash(Double.parseDouble(txtCash.getText()));
                paymentDTO.setBalance(Double.parseDouble(txtChange.getText()));

                orderDTO.setPaymentDTO(paymentDTO);

                List<OrderDetailsDTO> list = new ArrayList<>();
                for (int i = 0; i < dtm.getRowCount(); i++) {
                    ItemDTO searchItem = itemController.searchItem((String) dtm.getValueAt(i, 0));
                    searchItem.setQty(searchItem.getQty() - Integer.parseInt(dtm.getValueAt(i, 3).toString()));

                    OrderDetailsDTO detailsDTO = new OrderDetailsDTO();
                    detailsDTO.setItemDTO(searchItem);
                    detailsDTO.setOrderDTO(orderDTO);

                    list.add(detailsDTO);
                }
                orderDTO.setList(list);
                boolean addOrder = placeOrderController.addOrder(orderDTO);

                if (addOrder) {
                    JOptionPane.showMessageDialog(rootPane, "Added");
                    clearAlltextField();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Failed");
                }

            } catch (Exception ex) {
                Logger.getLogger(POS_Main_Cashier.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        PrintBill();
        clearAlltextField();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void txtQuantityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtQuantityActionPerformed

        if (Validation.numberOnly(txtQuantity)) {
            int x = Integer.parseInt(txtQuantity.getText());
            int qtyOnhand = Integer.parseInt(txtQuantityOnHand.getText()) - x;
            if (qtyOnhand >= 0) {
                txtQuantityOnHand.setText(Integer.toString(qtyOnhand));
                Double amount = x * Double.parseDouble(txtPrice.getText());
                if (cmbDressCode.getSelectedIndex() == -1) {
                    JOptionPane.showMessageDialog(null, "Item is not Selected");
                } else {

                    if (getDuplicateRow(cmbDressCode.getSelectedItem()) != -1) {
                        DefaultTableModel dtm = (DefaultTableModel) tblOrder.getModel();
                        int row = getDuplicateRow(cmbDressCode.getSelectedItem());
                        int newQty = (Integer.parseInt(dtm.getValueAt(row, 3).toString()) + Integer.parseInt(txtQuantity.getText()));
                        dtm.setValueAt(newQty, row, 3);
                        dtm.setValueAt((double) newQty * Double.parseDouble(txtPrice.getText()), row, 4);
                        setAmount();
                    } else {
                        Object[] rowData = {cmbDressCode.getSelectedItem(), txtDescription.getText(), txtPrice.getText(), x, amount};
                        dtm.addRow(rowData);
                        txtAmount.setText(Double.toString(amount));
                        txtTotItem.setText(Integer.toString(tblOrder.getRowCount()));
                        setAmount();
                    }
                    txtQuantity.setText("");
                    cmbDressCode.requestFocusInWindow();

                }
            } else {
                JOptionPane.showMessageDialog(null, "Items are not Available");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Input");
        }


    }//GEN-LAST:event_txtQuantityActionPerformed

    private void btnAddTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTableActionPerformed
        int x = Integer.parseInt(txtQuantity.getText());
        int qtyOnhand = Integer.parseInt(txtQuantityOnHand.getText()) - x;
        if (qtyOnhand >= 0) {
            txtQuantityOnHand.setText(Integer.toString(qtyOnhand));
            Double amount = x * Double.parseDouble(txtPrice.getText());
            if (cmbDressCode.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(null, "Item is not Selected");
            } else {
                Object[] rowData = {cmbDressCode.getSelectedItem(), txtDescription.getText(), txtPrice.getText(), x, amount};
                dtm.addRow(rowData);
                txtAmount.setText(Double.toString(amount));
                setAmount();
                txtTotItem.setText(Integer.toString(tblOrder.getRowCount()));

                txtQuantity.setText("");
                txtQuantityOnHand.setText("");
                txtPrice.setText("");
                txtDescription.setText("");

                cmbDressCode.requestFocusInWindow();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Items are not Available");
        }
    }//GEN-LAST:event_btnAddTableActionPerformed

